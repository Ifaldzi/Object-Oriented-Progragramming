    private void addOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrderButtonActionPerformed
        String name = "Nama: " + customerNameField.getText() + "\n";
        String address = "Alamat: " + addressField.getText() + "\n";
        String phone = "Telp: " + phoneField.getText() + "\n";
        
        String data = name + address + phone + "=================\n";
        if(steakCheckBox.isSelected()){
            data += "   - " + steakCheckBox.getText() + "\n";
        }
        if(sphagetiCheckBox.isSelected()){
            data += "   - " + sphagetiCheckBox.getText() + "\n";
        }
        if(pizzaCheckBox.isSelected()){
            data += "   - " + pizzaCheckBox.getText() + "\n";
        }
        data += "=================\n" + "Total Bayar:\n   RP. " + totalPay;
        
        dataTextArea.setText(data);
    }//GEN-LAST:event_addOrderButtonActionPerformed

