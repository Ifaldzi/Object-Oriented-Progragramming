public class ServicePackage 
{
    private String service;
    private float value;

    public String getService()
    {
        return this.service;
    }

    public float getValue()
    {
        return this.value;
    }

    public void setService(String service)
    {
        this.service = service;
    }

    public void setValue(float value)
    {
        this.value = value;
    }
}
