public class Status 
{
    private int code;
    private String description;

    public int getCode()
    {
        return this.code;
    }

    public String getDescription()
    {
        return this.description;
    }

    public void setCode(int code)
    {
        this.code = code;
    }

    public void setDescription(String description)
    {
        this.description = description;
    }
}
